/**
 * Convert boolean values to strings 'Yes' or 'No'.
 * @author Stanislav Rakitov
 */
class YesOrNo
{
  /**
   * Convert boolean values to strings 'Yes' or 'No'.
   *
   * @param b boolean value
   * @return "Yes" or "No"
   */
  public static String boolToWord(boolean b) {
    return b ? "Yes" : "No";
  }

}